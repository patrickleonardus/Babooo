package com.bantoo.babooo.Model;

public class User {
    public String name;
    public String email;
    public String phoneNumber;
    public String password;

    public User(String name, String email, String phoneNumber, String password) {
        this.name = name;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.password = password;
    }
}
